package finalhomework.tcl.com.finalhomework.pojo;


public class DataSum {

    private int recordDay;
    private int recordNumber;
    private float TotalIncome;
    private float TotalOutcome;

    public int getRecordDay() {
        return recordDay;
    }

    public void setRecordDay(int recordDay) {
        this.recordDay = recordDay;
    }

    public int getRecordNumber() {
        return recordNumber;
    }

    public void setRecordNumber(int recordNumber) {
        this.recordNumber = recordNumber;
    }

    public float getTotalIncome() {
        return TotalIncome;
    }

    public void setTotalIncome(float totalIncome) {
        TotalIncome = totalIncome;
    }

    public float getTotalOutcome() {
        return TotalOutcome;
    }

    public void setTotalOutcome(float totalOutcome) {
        TotalOutcome = totalOutcome;
    }
}
